export const HELP_TEXT = `# 📚 命令帮助

## 工作目录管理

**添加**: \`/d a <别名> <路径> [选项]\`
- 选项: \`-desc "描述"\` | \`-p\` | \`-cmd "命令"\` | \`-po 端口\` | \`-d\`
- 示例: \`/d a blog /path/to/blog -desc "博客" -p -cmd "npm run dev" -d\`

**列出**: \`/d ls\`
**详情**: \`/d i <别名>\`
**设为默认**: \`/d d <别名>\`
**更新**: \`/d u <别名> [选项]\`
- 选项: \`-desc "描述"\` | \`-p on|off\` | \`-cmd "命令"\` | \`-po 端口\`

**删除**: \`/d rm <别名>\`

## 预览管理

**启动**: \`/p s <别名>\` - 启动项目预览
**停止**: \`/p x <别名>\` - 停止指定项目
**停止全部**: \`/p xa\` - 停止所有预览
**状态**: \`/p st [别名]\` - 查看预览状态

## 使用示例

1. 添加项目: \`/d a blog /path/to/blog -p -cmd "npm run dev" -d\`
2. 查看配置: \`/d ls\`
3. 执行任务: \`[dir:blog] 帮我优化首页性能\` (完成后自动启动预览)
4. 管理预览: \`/p s blog\` → \`/p st\` → \`/p x blog\`
5. 默认目录: \`帮我检查代码\` (使用默认目录)

## 消息格式

**语法**: \`[dir:<别名>] [new] 任务内容\`
- \`[dir:<别名>]\` - 指定工作目录
- \`[new]\` - 开启新会话

**示例**:
\`\`\`
帮我检查代码                        # 默认目录
[dir:blog] 帮我添加新文章            # 指定目录
[new] 重新开始，分析项目             # 新会话
[dir:blog] [new] 从头分析博客        # 指定目录+新会话
\`\`\`
`;
